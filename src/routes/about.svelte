<p>David Kerpel has been practicing family and matrimonial law for over sixteen years. He is experienced in handling family law matters at every stage of the legal process. David's areas of expertise include divorce, paternity, child support and modification of child support, child custody and visitation, spousal support and maintenance, prenuptial agreements, division of marital and non-marital property, and orders of protection.</p>
<h6>Knowledgeable and Compassionate Representation</h6>
<p>A skillful and accomplished attorney, David Kerpel is also a staunch advocate for children. He will work tirelessly to achieve solutions that are in the best interests of his client and the children involved
<br><br>
David has been practicing in the area of family and matrimonial law for over sixteen years. He has worked at two of the largest matrimonial law firms in the Chicago area, before opening his own practice in 2003. David attended the University of Iowa for his undergraduate degree, and obtained his Juris Doctor from the DePaul School of Law. While attending DePaul, he was able to expand his knowledge of matrimonial law by participating in an externship with a Cook County Domestic Relations Judge. When preparing cases for trial, David combines his knowledge and experience with his attention to detail. David has successfully defended against and petitioned for orders of protection, and has successfully litigated both custody and financial trials.
<br><br>
David is admitted to the State of Illinois Bar, and is also licensed to practice law before the U.S. District Court, Northern District of Illinois, and the United States Supreme Court.
<br><br>
David has written articles for the Illinois State Bar Association and the Lake County Bar Association, and has lectured for the Lake County Bar Association and The Lilac Tree.
