      <div class="card-header">What is mediation, and when is it appropriate?</div>
      <div class="card-body">
        <p class="card-text">When the parties involved are able to communicate effectively, and trust each other to disclose all relevant information, mediation is a non-confrontational solution to resolving your marital difficulties. The Law Offices of David S. Kerpel offers the option of using skilled mediators when it is appropriate to the circumstances.</p>
      </div>
