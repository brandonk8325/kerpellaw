      <div class="card-header">How long will my divorce take?</div>
      <div class="card-body">
        <p class="card-text">There is no easy answer to this question although it is probably one of the most commonly asked. The duration of a divorce will depend on many factors. The most common questions to ask which will affect the duration of your divorce are:</p>
      <ul>
      <li>Is custody an issue?</li>
      <li>Is there a business to value?</li>
      <li>Is there a lot of real estate to value?</li>
      <li>Is dissipation an issue?</li>
      <li>Is maintenance or child support an issue?</li>
      <li>Is one of the spouses self-employed?</li>
      <li>Will the parties cooperate with the discovery process?</li>
      <li>Are the attorneys intent on litigating, or are they willing to enter into reasonable settlement discussions?</li>
      </ul>
