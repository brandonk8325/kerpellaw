    <div class="card border-light mb-3">
      <div class="card-header">How can I control my costs?</div>
      <div class="card-body">
        <p class="card-text">You can control costs by assisting your attorney and professionals associated with your proceedings. Gather your pertinent information and documents. Make your own copies. Set realistic goals. Be concise, and focus on facts relevant to your case.</p>
      </div>
    </div>
    <div class="card border-light mb-3">
      <div class="card-header">When is a neutral business or real estate appraiser appropriate?</div>
      <div class="card-body">
        <p class="card-text">Similar to participating in mediation, a neutral business or real estate appraiser should only be chosen when the parties have been completely forthright in the exchange of information, and are willing to compromise in their pursuit of litigating their respective rights. Agreeing to use a mutual expert can save parties thousands of dollars, and months and months of extended litigation.</p>
      </div>
    </div>
    <div class="card border-light mb-3">
      <div class="card-header">How can I modify support or custody orders?</div>
      <div class="card-body">
        <p class="card-text"> A support or custody order can only be modified by filing a petition usually in the court where the prior order establishing the custody or support order was entered. Especially important to remember is the fact that child support can only be modified from the time a petition is filed and noticed before the court. If you believe a payer's income has substantially increased since the last support order was entered, you should file the petition as soon as possible. If you choose to wait to file, you will not be able to obtain a retroactive increase from before the time you file the petition.</p>
      </div>
    </div>
    <div class="card border-light mb-3">
      <div class="card-header">What is discovery and when do I need it?</div>
      <div class="card-body">
        <p class="card-text">Discovery is the process by which your attorney obtains relevant information concerning every facet of your case. Discovery is probably the most important aspect of your case. Whether the issues of your divorce or paternity deal with finances, custody, visitation, or even grounds of your divorce, discovery is the process by which you obtain all of the relevant facts to support your case. Discovery can take many forms including depositions, written requests for information (often called interrogatories), and subpoenas.</p>
      </div>
    </div>
    <div class="card border-light mb-3">
      <div class="card-header">What is dissipation?</div>
      <div class="card-body">
        <p class="card-text">Dissipation is strictly defined as property which is improperly used for the sole benefit of one spouse, for a purpose unrelated to the marriage, at a time when the marriage is undergoing an irretrievable breakdown. Some commons examples of dissipation include:</p>
        <ul>
        <li>Spending money on a boyfriend or girlfriend</li>
        <li>Gambling losses</li>
        <li>Reckless investing of funds</li>
        <li>Destruction of personal property</li>
        <li>Failing to maintain the mortgage on a home</li>
        </ul>
